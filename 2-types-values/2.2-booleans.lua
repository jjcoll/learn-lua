The bollean type has two values, false and true, whih represents the traditional boolean values. However, they do not hold a monopoly of condition values:
In Lua, any value may represent a condition. Conditionals (such as the ones in control structures) consider false and nil as
false and anything else as true ( including 0 ). This means that unlike other scripting languages, Lua considers both
zero and the empty string as true in conditional tests. 
